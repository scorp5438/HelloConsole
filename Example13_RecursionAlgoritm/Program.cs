void FillImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0 || image[i, j] == 6) Console.Write($"   ");
            else if (image[i, j] == 1)  Console.Write("/~\\");
            else if (image[i, j] == 2) Console.Write(" - ");
            else if (image[i, j] == 3) Console.Write(" ^ ");
            else if (image[i, j] == 4) Console.Write(" . ");
            else if (image[i, j] == 5) Console.Write(" + ");
            else if (image[i, j] == 7) Console.Write("__");
            else if (image[i, j] == 8) Console.Write("| ");
            else if (image[i, j] == 9) Console.Write("\\ ");
        }
        Console.WriteLine();
    }
}
void Fill(int row, int col, int num)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = num;
        Fill(row - 1, col, num);
        Fill(row, col - 1, num) ;
        Fill(row + 1, col, num);
        Fill(row, col + 1, num);
    }
}

